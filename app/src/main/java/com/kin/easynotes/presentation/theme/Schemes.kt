fun getDarkDynamicColorScheme(context: Context): ColorScheme {
    val scheme = dynamicDarkColorScheme(context)

    return scheme.copy(
        background = scheme.surfaceContainerLow.editRGB(blue = -2F),
        surfaceContainer = scheme.surfaceContainerHigh.editRGB(blue = -1F).darken(0.12f),
        surfaceContainerLow = scheme.surfaceContainerLow.editRGB(blue = -2F).darken(0.23f),
        surfaceContainerHigh = scheme.surfaceContainerLow.editRGB(blue = -2F),
        onSurfaceVariant = scheme.onSurface.darken(0.2f),
        primary = scheme.primary
    )
}


fun getLightDynamicColorScheme(context: Context): ColorScheme {
    val scheme = dynamicLightColorScheme(context)

    return scheme.copy(
        surfaceContainerHigh = scheme.surfaceContainerHighest,
        surfaceContainerLow = scheme.surfaceContainerHigh,
        surfaceContainer = scheme.surfaceContainerHigh,
        onSurface = scheme.onSurfaceVariant
    )
}